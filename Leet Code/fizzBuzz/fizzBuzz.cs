public class Solution {
    public IList<string> FizzBuzz(int n) {
        IList<string> myList=new List<string>();
        for (int i=1; i<=n; i++){
            string addMe="";
            if (i%3==0) addMe+="Fizz";
            if (i%5==0) addMe+="Buzz";
            if (i%5!=0 && i%3!=0) addMe=i.ToString();
            myList.Add(addMe);
        }
        return myList;
    }
}
